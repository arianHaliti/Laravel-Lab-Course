    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
       
        if(!is_numeric($id))
            abort(404);
        $lesson = Lesson::find($id);
        $course_id = $lesson->course_id;
        $course =  Course::find($course_id);


        if(count($course)==0)
          abort(404);

        $data = [
            'lesson' => $lesson,
            'course' => $course
        ];
        return view('courses.showC')->with('data',$data);

    }

